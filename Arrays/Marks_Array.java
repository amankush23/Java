class Marks_Array{
    public static void main(String[] args){
        int[] marks = new int[3];
        marks[0] = 97; //Physics
        marks[1] = 99; //English
        marks[2] = 87; //Hindi
        for(int i = 0; i <= 3; i++){
            System.out.println(marks[i]);
        }
        
    }
}